#ifndef TYPES_H_
#define TYPES_H_

#include <stdint.h>

/* Use these typedef types, this makes it clear for byte sizes and requires less typing */
typedef uint8_t u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef uint64_t u64;

typedef int8_t i8;
typedef int16_t i16;
typedef int32_t i32;
typedef int64_t i64;

typedef float f32;
typedef double f64;



#endif